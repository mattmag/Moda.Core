namespace Moda.Core.Utility.Geometry;

/// <summary>
///     Enumerates the 4 corners of a rectangle.
/// </summary>
public enum Corner
{
    /// <summary>
    ///     The top-left corner.
    /// </summary>
    TopLeft,

    /// <summary>
    ///     The top-right corner.
    /// </summary>
    TopRight,

    /// <summary>
    ///     The bottom-right corner.
    /// </summary>
    BottomRight,

    /// <summary>
    ///     The The bottom-left corner.
    /// </summary>
    BottomLeft,
}
